							<p>It was certainly alarming when Julian Assange, a board member for the secret document sharing site WikiLeaks, alleged in a statement last week that his organization was “currently under a US and Icelandic aggressive surveillance operation.”</p>

<p>WikiLeaks, a non-profit, has draw attention and praise from many quarters (<a href=http://www.cjr.org/the_kicker/wikileaked_onto_a10.php  target=_blank>including</a> <a href=http://www.cjr.org/the_kicker/dont_worry_WikiLeaks.php target=_blank>CJR</a>) for providing safe harbor to whistleblowers with hot documents that others would rather keep hidden. The site has published important information on U.S. policies in Iraq and at Guantanamo Bay, Swiss bank records, and so on, generating stories in just about every major outlet across the globe.</p>

<p>Recently, Assange, an Australian national and WikiLeaks’s <I>de facto</I> chief spokesperson, has been spending some time in Iceland working with Parliament members and others to encourage the adoption of a package of reforms that would create the world’s most publisher- and journalist-friendly freedom of information, libel, and source protection laws. Together it is hoped that they would make the sparsely populated island nation something of a journalistic haven. </p>

<p>During his time in Iceland, Assange reports discovering “half a dozen attempts at covert surveillance in Reykjavik both by native English speakers and Icelanders” targeted at WikiLeaks. The charges come as the site is engaged in a <a href=http://stefanmey.wordpress.com/2010/01/04/leak-o-nomy-the-economy-of-wikileaks/ target=_blank>fundraising drive</a> that has caused it to suspend aspects of its normal operations.</p>

<p>Assange’s perhaps most alarming report of any such surveillance begins on the night of March 22, when a teenaged WikiLeaks volunteer was detained by Icelandic police. In the course of the detention, Assange says, the volunteer was shown photos of Assange leaving a Reykjavik restaurant, where a WikiLeaks team had been meeting to discuss the release of undisclosed Pentagon video showing a U.S. air strike. In the interrogation, according to Assange’s syntax-challenged telling, the police also identified others who’d been at the meeting, using information they gained when they “spied on production meeting used a discreet, closed, backroom,” and mentioned the existence of the video, a fact that Assange forebodingly writes “could not have been understood from that day’s exterior surveillance alone.”</p>

<p>The week before the meeting and the volunteer’s arrest, Assange left Iceland to speak at an investigative media conference in Norway. Assange writes that after “receiving a tip” he obtained flight manifest records showing that two individuals “operating under the name of the U.S. State Department” boarded his outbound flight. </p>

<p>All this comes just a week after WikiLeaks’s March 15 publication of a 36-page classified <a href=http://file.wikileaks.org/file/us-intel-wikileaks.pdf target=_blank>report</a>, prepared in 2008 by an analyst in the Army’s Counterintelligence Center, on the potential threat that WikiLeaks represented to Army operations. Assange has claimed the document contains a “plan” by U.S. intelligence to “destroy” and “hack” WikiLeaks.</p>

<p>Assange further suggests that the increased scrutiny he describes may be in response to WikiLeaks’s possession of the video, which he says shows a “massacre” of civilians by U.S. pilots, <a href=http://twitter.com/WikiLeaks/status/7530875613 target=_blank>likely</a> depicting a notorious May 2009 air strike on the Afghan village of Garani <a href=http://www.wired.com/dangerroom/2009/06/afghan-airstrike-video-goes-down-the-memory-hole  target=_blank>that may have killed close to 100 civilians</a>. </p>

<p>While there’s been next to nothing written about it in English, the Icelandic press took a look at Assange’s charges of being surveilled in Iceland last week—and, at best, have found nothing to substantiate them.</p>

<p>Again, one of his most specific charges—that authorities spied on a private meeting and that he was photographed on the street emerging from this meeting—stems from his retelling of his teenage associate’s encounter with Icelandic police.  </p>

<p>The police have confirmed to Icelandic journalists that the teenaged volunteer was indeed arrested and questioned.  But according to Ingólfur Bjarni Sigfússon, the deputy head of news for the RUV national broadcast service, he was accompanied to the interview by his father and, in accordance with Icelandic procedures on the arrest of minors, a government child services worker.</p>

<p>According to Stigur Helgason, who’s written about Assange’s allegations for <i>Frettabladid</i>, Iceland’s largest paper, the arrest “happened when the kid was allegedly trying to break into a company out in the suburbs of Reykjavik”—a paint company where his father works.</p>

<p>This was not the teenager’s first arrest. He landed on Iceland’s front pages in January after trying to sell news outlets financial records that he allegedly stole from his employer, an investment company key to Iceland’s economic collapse. </p>

<p>“This kid is apparently a couple of sandwiches short of a picnic, and is quite clever at coming up with stories,” says Ingólfur.</p>

<p>“This kid has had quite a bit of problems as of late,” says Helgason. “He has checked into a rehabilitation center for youths.”</p>

<p>The police told Helgason they never asked the teenager any questions about WikiLeaks, and that the teenager brought up the organization, claiming that a laptop he had on him at the time of his arrest was owned by WikiLeaks.</p>

<p>In regards to the laptop, Helgason says “we only have the word of the police for what he said.  And if he said it, we don’t know if it’s the truth.”</p>

<p>More broadly, the police and the Icelandic Minster of Justice denied to Hegalson that they are involved in any investigation of WikiLeaks.</p>

<p>Assange or someone else writing at WikiLeaks’ official Twitter account admitted that perhaps not all about the interrogation was as originally suggested. On March 28, after Icelandic journalists had had a chance to report out his claims, the account was updated with <a href=http://twitter.com/wikileaks/statuses/11230678157  target=_blank>the following</a>:<br />
 <br />
<blockquote>There is debate as to what happened during the interrogation in Iceland [not other matters]. We are seeking clarification.</blockquote></p>

<p>Whatever clarification Assange may have sought, he hasn’t offered any himself—the WikiLeaks site and Twitter feed have since been essentially silent on Assange’s surveillance charges.  (Hegalson and Ingólfur’s attempts to reach Assange failed or went without response, as did CJR’s.) </p>

<p>And while Assange’s brackets insist that there is no debate about other surveillance matters, Icelandic journalists have been unable to substantiate his claims about the two people “operating under the name of the U.S. State Department” who boarded his flight to Norway. </p>



					
			                
		
								
		
								
					
