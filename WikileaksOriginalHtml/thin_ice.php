				<h3 class="title">Thin Ice</h3>
				
				<p class="subhead">The man behind WikiLeaks has some allegations</p>
				
				<p class="attribution">By <a href="http://www.cjr.org/author/clint-hendler-1/">Clint Hendler</a></p>
				
				                     
                    
                  
                    
                    
